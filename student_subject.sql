-- Note the sequence of table construction:
-- Student, Subject, StudentSubject

-- Create the 'Student' table
CREATE TABLE Student (
    stu_id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    cg TEXT NOT NULL,
    bmi REAL,
    Amath TEXT CHECK(Amath IN ('A1', 'A2', 'B3', 'B4', 'C5', 'C6', 'D7', 'E8', 'F9')),
    Emath TEXT CHECK(Emath IN ('A1', 'A2', 'B3', 'B4', 'C5', 'C6', 'D7', 'E8', 'F9')),
    rank_pt INTEGER CHECK(rank_pt BETWEEN 0 AND 70)
);

-- Create the 'Subject' table
CREATE TABLE Subject (
    subject_code TEXT PRIMARY KEY,
    subject TEXT NOT NULL,
    h_level TEXT CHECK(h_level IN ('H1', 'H2'))
);

-- Create the 'StudentSubject' link table
CREATE TABLE StudentSubject (
    stu_id TEXT,
    subject_code TEXT,
    FOREIGN KEY(stu_id) REFERENCES Student(stu_id),
    FOREIGN KEY(subject_code) REFERENCES Subject(subject_code),
    PRIMARY KEY (stu_id, subject_code)
);

